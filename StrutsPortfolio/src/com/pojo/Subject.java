package com.pojo;

public class Subject {

	private String subject_code;
	private String subject_name;
	private int programme_code;
	private String programme_name;
	private int branch_code;
	private String branch_name;
	private String status;
	
	public String getSubject_code() {
		return subject_code;
	}
	public void setSubject_code(String subject_code) {
		this.subject_code = subject_code;
	}
	public String getSubject_name() {
		return subject_name;
	}
	public void setSubject_name(String subject_name) {
		this.subject_name = subject_name;
	}
	public int getProgramme_code() {
		return programme_code;
	}
	public void setProgramme_code(int programme_code) {
		this.programme_code = programme_code;
	}
	public String getProgramme_name() {
		return programme_name;
	}
	public void setProgramme_name(String programme_name) {
		this.programme_name = programme_name;
	}
	public int getBranch_code() {
		return branch_code;
	}
	public void setBranch_code(int branch_code) {
		this.branch_code = branch_code;
	}
	public String getBranch_name() {
		return branch_name;
	}
	public void setBranch_name(String branch_name) {
		this.branch_name = branch_name;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	

	
}
